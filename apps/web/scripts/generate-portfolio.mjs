import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appDir = path.resolve(scriptDir, "..");
const portfolioDir = path.join(appDir, "public", "portfolio");
const outFile = path.join(appDir, "src", "content", "portfolio.generated.ts");
const supportedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp"]);

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);

      if (entry.isDirectory()) {
        return walk(fullPath);
      }

      if (supportedExtensions.has(path.extname(entry.name).toLowerCase())) {
        return [fullPath];
      }

      return [];
    })
  );

  return files.flat();
}

function readJpegSize(buffer) {
  if (buffer[0] !== 0xff || buffer[1] !== 0xd8) {
    return null;
  }

  let offset = 2;
  while (offset < buffer.length) {
    if (buffer[offset] !== 0xff) {
      offset += 1;
      continue;
    }

    const marker = buffer[offset + 1];
    const size = buffer.readUInt16BE(offset + 2);

    if (
      marker === 0xc0 ||
      marker === 0xc1 ||
      marker === 0xc2 ||
      marker === 0xc3
    ) {
      return {
        height: buffer.readUInt16BE(offset + 5),
        width: buffer.readUInt16BE(offset + 7)
      };
    }

    offset += size + 2;
  }

  return null;
}

function readPngSize(buffer) {
  const signature = "89504e470d0a1a0a";
  if (buffer.subarray(0, 8).toString("hex") !== signature) {
    return null;
  }

  return {
    width: buffer.readUInt32BE(16),
    height: buffer.readUInt32BE(20)
  };
}

function readWebpSize(buffer) {
  if (
    buffer.subarray(0, 4).toString("ascii") !== "RIFF" ||
    buffer.subarray(8, 12).toString("ascii") !== "WEBP"
  ) {
    return null;
  }

  const chunk = buffer.subarray(12, 16).toString("ascii");

  if (chunk === "VP8X") {
    return {
      width: 1 + buffer.readUIntLE(24, 3),
      height: 1 + buffer.readUIntLE(27, 3)
    };
  }

  if (chunk === "VP8 ") {
    return {
      width: buffer.readUInt16LE(26) & 0x3fff,
      height: buffer.readUInt16LE(28) & 0x3fff
    };
  }

  if (chunk === "VP8L") {
    const bits = buffer.readUInt32LE(21);
    return {
      width: (bits & 0x3fff) + 1,
      height: ((bits >> 14) & 0x3fff) + 1
    };
  }

  return null;
}

function readImageSize(buffer) {
  return readJpegSize(buffer) ?? readPngSize(buffer) ?? readWebpSize(buffer);
}

function slugFromSource(src) {
  return src
    .replace(/^\/portfolio\//, "")
    .replace(/\.[a-z0-9]+$/i, "")
    .replace(/[^a-zA-Z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

function titleFromSlug(slug) {
  const title = slug
    .split("-")
    .filter((part) => !/^\d{6,}$/.test(part) && part !== "june" && part !== "2026")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

  return title || "Tile Project Photo";
}

const files = (await walk(portfolioDir)).sort((a, b) => a.localeCompare(b));
const images = await Promise.all(
  files.map(async (file) => {
    const buffer = await readFile(file);
    const size = readImageSize(buffer);

    if (!size) {
      throw new Error(`Could not read image dimensions for ${file}`);
    }

    const relative = path.relative(path.join(appDir, "public"), file);
    const src = `/${relative.split(path.sep).join("/")}`;
    const id = slugFromSource(src);

    return {
      id,
      src,
      title: titleFromSlug(id),
      width: size.width,
      height: size.height,
      orientation: size.width >= size.height ? "landscape" : "portrait"
    };
  })
);

const content = `// This file is generated by apps/web/scripts/generate-portfolio.mjs.
// Do not edit by hand. Add images to public/portfolio and run the generator.

export type GeneratedPortfolioImage = {
  readonly id: string;
  readonly src: string;
  readonly title: string;
  readonly width: number;
  readonly height: number;
  readonly orientation: "landscape" | "portrait";
};

export const generatedPortfolioImages = ${JSON.stringify(images, null, 2)} as const satisfies readonly GeneratedPortfolioImage[];
`;

await writeFile(outFile, content);
console.log(`Generated ${images.length} portfolio images`);
